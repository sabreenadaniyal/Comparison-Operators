"use strict";
//COMPARISION OPERATORS
//Equal to(==):
let a = 20;
let b = 8;
console.log(a == b);
//Not Equalto(!==):
let c = 18;
let d = 5;
console.log(c != d);
//Equal to & data type(===):
let e = 14;
let f = "8";
//@ts-ignore
console.log(e === f);
//Not equalto & data type(!==):
let g = 25;
let h = 25;
console.log(g !== h);
//Lessthan(<):
let i = 5;
let j = -50;
console.log(i < j);
//Greaterthan(>):
let k = 45;
let l = 30;
console.log(k > l);
//Lessthan & data type(<=):
let m = 50;
let n = 32;
console.log(m <= n);
//Greaterthan & data type(>=):
let o = 180;
let p = 150;
console.log(o >= p);
