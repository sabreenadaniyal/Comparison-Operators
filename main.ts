//COMPARISION OPERATORS
//Equal to(==):
let a:number=20
let b:number=8
console.log(a == b);  

//Not Equalto(!==):
let c:number=18
let d:number=5
console.log(c != d);

//Equal to & data type(===):
let e:number=14
let f:string="8"
//@ts-ignore
console.log(e === f);

//Not equalto & data type(!==):
let g:number=25
let h:number=25
console.log(g !== h)

//Lessthan(<):
let i:number=5
let j:number=-50
console.log(i < j);

//Greaterthan(>):
let k:number=45
let l:number=30
console.log(k > l);

//Lessthan & data type(<=):
let m:number=50
let n:number=32
console.log(m <= n);

//Greaterthan & data type(>=):
let o:number=180
let p:number=150
console.log(o >= p);

